/**
 * Builds public/landing-legal-i18n.js from scripts/landing-legal-i18n.mjs
 * Run from repo root: node scripts/build-landing-legal-i18n.mjs
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import legal from "./landing-legal-i18n.mjs";

const root = dirname(fileURLToPath(import.meta.url));
const out = join(root, "..", "public", "landing-legal-i18n.js");

writeFileSync(
  out,
  `"use strict";\n/** Generated by scripts/build-landing-legal-i18n.mjs — do not edit by hand. */\nglobalThis.MPV_LANDING_LEGAL = ${JSON.stringify(legal, null, 2)};\n`,
  "utf8",
);

console.log("Wrote public/landing-legal-i18n.js");
